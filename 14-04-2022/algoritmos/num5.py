# Faça um algoritmo que calcule a área de um terreno. Os terrenos são
# retangulares, e deverão ser informadas as medidas de largura e comprimento do
# terreno.

largura = float(input("Largura do terreno: "))
comprimento = float(input("Comprimento do terreno: "))
print("Área do terreno retangular:",largura * comprimento,"m")